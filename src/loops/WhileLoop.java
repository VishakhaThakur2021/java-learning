package loops;

public class WhileLoop {

  public static void main(String[] args) {
    int coffee = 1;
    // add while loop with counter
    while (coffee <= 100) {
      System.out.println("Fry drinks cup of coffee #1");
      coffee = coffee + 1;
      coffee++;
      System.out.println("Fry drinks cup of coffee #100");

    }
  }
}

